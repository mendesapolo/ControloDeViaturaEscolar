    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if (!"Novo".equalsIgnoreCase(txtId.getText()) && !txtIdp.getText().equals("")) {
            if (!txtBi.getText().equals("") && !txtTelefone.getText().equals("") && !txtNome.getText().equals("")) {
                if (cbViaturas.getSelectedIndex() >= 0) {
                    Pessoa p = new Pessoa(Integer.parseInt(txtIdp.getText()), txtNome.getText(), txtBi.getText(), txtTelefone.getText(), "motorista");
                    if(PessoaDao.updade(p)){
                        Motorista m = new Motorista(Integer.parseInt(txtId.getText()), p, (Viatura) cbViaturas.getSelectedItem());
                        if (MotoristaDao.updade(m)) {
                            JOptionPane.showMessageDialog(this, "Atualizado com Sucesso", "Sucesso", 1);
                            refreshTb();
                            novo();
                        } else {
                            JOptionPane.showMessageDialog(this, "Erro ao salvar os dados pessoais", "ERRO", 0);
                        }
                    }else{
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Selecione uma Viatura para o motorista \nque pretende atualizar", "VIATURA", 2);
                JOptionPane.showMessageDialog(this, "Preencha todos os campos", "Campos Vazios", 2);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Preencha todos os campos", "Campos Vazios", 2);
                txtNome.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(this, "NÃ£o foi selecionado nenhum registro", "Sem Registro", 2);
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

